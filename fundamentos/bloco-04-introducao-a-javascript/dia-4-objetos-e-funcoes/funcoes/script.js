function verificaPalindromo (string) {
    let reverse = string.split('').reverse().join('');
    let palindromo = reverse === string ? true : false;

    return palindromo;
}

console.log(verificaPalindromo('arara'));
console.log(verificaPalindromo('desenvolvimento'));

